;------------------------------------------------------------------
;  每个文件分配区占用32字节，每个扇区支持16个文件
;  2字节开始+16字节文件名+8字节LBA开始地址+4字节读取扇区数+2字节结束
;
;------------------------------------------------------------------

[bits 16]
org 0x1500 ; 设置本段初始地址(MBR启动时加载)


;------------------------------------------------------------------
; 屏幕测试
;2字节开始
db 0xcc,0xcc
;16字节文件名
screen:
db 'testscreen',13
times 16-($-screen) db 0
;8字节LBA开始地址
db 10,0x00,0x00,0x00,0x00,0x00,0x00,0x00
;4字节读取扇区数
db 8,0x00,0x00,0x00
;2字节结束
db 0xff,0xff

;------------------------------------------------------------------
; 屏幕测试2
;2字节开始
db 0xcc,0xcc
;16字节文件名
screen2:
